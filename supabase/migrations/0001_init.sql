-- =====================================================================
-- SUBMISSIONS — quest proof uploads, approved by admin
-- =====================================================================
create table if not exists public.submissions (
  id              uuid primary key default gen_random_uuid(),
  user_id         uuid not null references public.profiles(id) on delete cascade,
  participant_id  uuid not null references public.participants(id) on delete cascade,
  quest_id        uuid not null references public.quests(id) on delete cascade,
  proof_url       text not null,
  status          text not null default 'pending'
                    check (status in ('pending', 'approved', 'rejected')),
  created_at      timestamptz not null default now()
);

create index if not exists idx_submissions_status      on public.submissions(status);
create index if not exists idx_submissions_user        on public.submissions(user_id);
create index if not exists idx_submissions_participant on public.submissions(participant_id);

-- =====================================================================
-- DAILY_VOTES — +5 points / day.
-- Dual uniqueness is the core anti-cheat:
--   (user_id, vote_date)            => one account can vote once/day
--                                      even if it switches devices.
--   (device_fingerprint, vote_date) => one device can vote once/day
--                                      even if it switches accounts.
-- =====================================================================
create table if not exists public.daily_votes (
  id                  uuid primary key default gen_random_uuid(),
  user_id             uuid not null references public.profiles(id) on delete cascade,
  participant_id      uuid not null references public.participants(id) on delete cascade,
  device_fingerprint  text not null,
  server_hash         text,
  vote_date           date not null default current_date,
  created_at          timestamptz not null default now(),
  unique (user_id, vote_date),
  unique (device_fingerprint, vote_date)
);

create index if not exists idx_daily_votes_participant on public.daily_votes(participant_id);
create index if not exists idx_daily_votes_date        on public.daily_votes(vote_date);
create index if not exists idx_daily_votes_user        on public.daily_votes(user_id);

